#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*p;
	char	*q;

	p = (char *)dst;
	q = (char *)src;
	while (*q != '\0')
	{
		*p = *q;
		p++;
		q++;
	}
	*p = '\0';
	return (dst);
}
