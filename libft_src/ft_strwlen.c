#include "libft.h"

size_t	ft_strwlen(wchar_t *str)
{
	size_t i;
	size_t nb;

	nb = 0;
	i = 0;
	while (str[i])
	{
		nb += ft_wchar_len(str[i]);
		i++;
	}
	return (nb);
}
