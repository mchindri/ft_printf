int	ft_strequ(char const *s1, char const *s2)
{
	char *p;
	char *q;

	if (!s1 || !s2)
		return (0);
	p = (char *)s1;
	q = (char *)s2;
	while (*p || *q)
		if (*p != *q)
			return (0);
		else
		{
			p++;
			q++;
		}
	return (1);
}
